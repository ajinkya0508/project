package com.entity;

public class Destination {
	private int id;
	private String name;
	private String landmarks;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName() {
		this.name = name;
	}
	public String getLandmarks() {
		return landmarks;
	}
}